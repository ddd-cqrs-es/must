namespace Nohros
{
  /// <summary>
  /// Defines the small unit of a date range.
  /// </summary>
  public enum DateGranularity
  {
    /// <summary>
    /// Represents a second
    /// </summary>
    Second = 1,

    /// <summary>
    /// Represents a minute
    /// </summary>
    Minute = 2,

    /// <summary>
    /// Represents an hour
    /// </summary>
    Hour = 3,

    /// <summary>
    /// Represents a day.
    /// </summary>
    Day = 0,

    /// <summary>
    /// Represents a month.
    /// </summary>
    Month = 5,

    /// <summary>
    /// Represents a year.
    /// </summary>
    Year = 6
  }
}
